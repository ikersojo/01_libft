#include "libft.h"

/* DESCRIPTION:
toupper() converts the letter c to upper case, if possible.
----------------------------------------------------------------------------- */
char	ft_toupper(char c)
{
	if (c >= 'a' && c <= 'z')
		return (c - 32);
	return (c);
}